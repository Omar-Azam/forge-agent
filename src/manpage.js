/**
 * Return instructions for installing the man page
 */
function generateInstallInstructions() {
  return [
    '# Install man page',
    'forge-agent --man > /tmp/forge-agent.1',
    'sudo gzip /tmp/forge-agent.1',
    'sudo mv /tmp/forge-agent.1.gz /usr/local/share/man/man1/',
    'sudo mandb',
    '# Now use it:',
    'man forge-agent'
  ].join('\n');
}

